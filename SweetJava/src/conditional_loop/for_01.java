package conditional_loop;

public class for_01 {
	public static void main(String args[]) {
		for (int cnt = 0; cnt < 10; cnt++) {
			System.out.println(cnt);
		}
		System.out.println("End.");

		System.out.println("===================");
		for (int cnt = 9; cnt >= 0; cnt--) {
			System.out.println(cnt);
		}
		System.out.println("End.");
	}
}