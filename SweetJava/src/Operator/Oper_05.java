package Operator;

public class Oper_05 {
	public static void main(String args[]) {
		int num = 0;
	// num의 값  증가
//		num2 = num + 1
//		System.out.println(num2);
		num = num + 1;
		System.out.println(num);
		num = num - 1;
		System.out.println(num);
		num++; //num = num + 1;
		System.out.println(num);
		++num; //num = num + 1;
		System.out.println(num);
		num--; //num = num + 1;
		System.out.println(num);
		--num; //num = num + 1;
		System.out.println(num);
	}
}
