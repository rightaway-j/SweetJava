package Operator;

public class Oper01_2 {
	public static void main(String args[]) {
		int a = 10, b = 4; 
			System.out.println(a + "+" + b + "=" + (a + b));
	 
			System.out.println(a + "-" + b + "=" + (a - b));
	
			System.out.println(a + "*" + b + "=" + a * b);
	  
			System.out.println(a + "/" + b + "=" + a/b);
		
			System.out.println(a + "%" + b + "=" + a%b);
	}
}
		
